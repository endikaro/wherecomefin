package datos;

public class Refugiado {
	
	private String nombre;
	private String apellido;
	private int id;
	private String sexo;
	private float altura;
	private float peso;
	private String estado_civil;
	private String fecha_nacimiento;
	private String lugar_registro;
	private String localizacion_actual;
	private String nacionalidad;
	private String telefono;
	private String ciudad_origen;
	private String email;
	private String nombre_usuario;
	private String pass;
	
	
	
	
	public float getAltura() {
		return altura;
	}
	public void setAltura(float altura) {
		this.altura = altura;
	}
	public float getPeso() {
		return peso;
	}
	public void setPeso(float peso) {
		this.peso = peso;
	}
	public String getEstado_civil() {
		return estado_civil;
	}
	public void setEstado_civil(String estado_civil) {
		this.estado_civil = estado_civil;
	}
	public String getFecha_nacimiento() {
		return fecha_nacimiento;
	}
	public void setFecha_nacimiento(String fecha_nacimiento) {
		this.fecha_nacimiento = fecha_nacimiento;
	}
	public String getLugar_registro() {
		return lugar_registro;
	}
	public void setLugar_registro(String lugar_registro) {
		this.lugar_registro = lugar_registro;
	}
	public String getLocalizacion_actual() {
		return localizacion_actual;
	}
	public void setLocalizacion_actual(String localizacion_actual) {
		this.localizacion_actual = localizacion_actual;
	}
	public String getNacionalidad() {
		return nacionalidad;
	}
	public void setNacionalidad(String nacionalidad) {
		this.nacionalidad = nacionalidad;
	}
	public String getTelefono() {
		return telefono;
	}
	public void setTelefono(String telefono) {
		this.telefono = telefono;
	}
	public String getCiudad_origen() {
		return ciudad_origen;
	}
	public void setCiudad_origen(String ciudad_origen) {
		this.ciudad_origen = ciudad_origen;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getPass() {
		return pass;
	}
	public void setPass(String pass) {
		this.pass = pass;
	}

	
	
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
	this.nombre = nombre;
	}
	public String getApellido() {
		return apellido;
	}
	public void setApellido(String apellido) {
		this.apellido = apellido;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getSexo() {
		return sexo;
	}
	public void setSexo(String sexo) {
		this.sexo = sexo;
	}
	public String getNombre_usuario() {
		return nombre_usuario;
	}
	public void setNombre_usuario(String nombre_usuario) {
		this.nombre_usuario = nombre_usuario;
	}
	

}
